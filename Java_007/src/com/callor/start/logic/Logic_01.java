package com.callor.start.logic;

/*
 * 논리(Logic)연산
 * 참(yes, true), 거짓(no, false)를 판단하여 
 * 어떤 행위를 수행하는 연산
 */
public class Logic_01 {
	
	public static void main(String[] args) {
		
		// 3이 3미만인가 (보다 작은가)
		System.out.println( 3 < 3 );
		System.out.println( 3 > 3 );
		
		//3이 3인가
		System.out.println( 3 == 3 );
		
		//3이 3보다 작거나 같은가(보다 작거나 같은가)
		System.out.println( 3 <= 3 );
		
		//3이 3이상(보다 크거나 같은가)
		System.out.println( 3 >= 3 );
		
		
	}

}
